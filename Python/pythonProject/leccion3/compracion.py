a = 4
b = 2
#Comparacion
res = (a == b)
print(f'Resultado == : {res}')
#Diferente
res = (a != b)
print(f'Resultado != : {res}')
#mayor
res = (a > b)
print(f'Resultado >: {res}')
#Menor
res = (a < b)
print(f'Resultado >: {res}')
#Mayor igual
res = (a >= b)
print(f'Resultado >= : {res}')
#menor igual
res = (a <= b)
print(f'Resultado <= : {res}')