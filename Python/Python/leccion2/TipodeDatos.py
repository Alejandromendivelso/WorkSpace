x = 11
print(x)
print(type(x))  # Dato de tipo entero (int)

y = 'hola mundo'
print(y)
print(type(y))  # Dato de tipo Texto (str)

z = 10.5
print(z)
print(type(z))  # Dato de tipo decimal (float)

a = False
print(a)
print(type(a))  # Dato de tipo booleano (bool)

# cadenas (String)
